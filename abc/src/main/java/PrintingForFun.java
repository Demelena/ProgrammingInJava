public class PrintingForFun {
    public static void main(String[] args) {

        // Display "Java is fun!"
        System.out.println("Java is fun!");
        toomuch();
        dont();
    }

    private static void toomuch(){

        //Display "Maybe, a little too much fun."
        System.out.println ("Maybe, a little too much fun.");

    }

    private static void dont(){

        //Display "Dont have too much fun!"
        System.out.println ("Dont have too much fun!");

    }
}
